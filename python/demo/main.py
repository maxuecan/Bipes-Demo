# 配置参数
char_spacing = 1  # 字间距
char_size = 16    # 16x16汉字
scroll_speed = 1  # 滚动速度（像素/帧）
purple = (128, 0, 128)  # 紫色RGB值

# 绘制时间字符
def draw_time_char(oled, char, x, y):
    if char not in chinese_font:
        return x
    
    data = chinese_font[char]
    for col in range(16):
        byte1 = data[col * 2]
        byte2 = data[col * 2 + 1]
        
        draw_x = x + col
        for row in range(8):
            if byte1 & (1 << row):
                oled.pixel(draw_x, y + row * 2, 1)
                oled.pixel(draw_x, y + row * 2 + 1, 1)
                
        for row in range(8):
            if byte2 & (1 << row):
                oled.pixel(draw_x, y + 16 + row * 2, 1)
                oled.pixel(draw_x, y + 16 + row * 2 + 1, 1)
    
    return x + 18

# 绘制16x16汉字（阳码取模适配）
def draw_16x16_char(oled, char_index, x, y):
    if 0 <= char_index < len(zh16x16):
        char_data = zh16x16[char_index]
        for col in range(16):
            byte1 = char_data[col * 2]
            byte2 = char_data[col * 2 + 1]
            
            for row in range(8):
                if byte1 & (1 << row):
                    oled.pixel(x + col, y + row, 1)
            for row in range(8):
                if byte2 & (1 << row):
                    oled.pixel(x + col, y + 8 + row, 1)

# LED控制函数
def control_leds():
    # 读取控制引脚状态
    if control_pin.value() == 1:  # 高电平
        # 所有灯珠设为紫色
        for i in range(LED_COUNT):
            np[i] = purple
        np.write()  # 刷新显示
    else:  # 低电平（默认）
        # 所有灯珠熄灭
        for i in range(LED_COUNT):
            np[i] = (0, 0, 0)
        np.write()  # 刷新显示

# 初始化硬件
def init_hardware():
    i2c_oled = I2C(0, sda=Pin(0), scl=Pin(1), freq=400000)
    oled = SSD1306_I2C(128, 64, i2c_oled, addr=0x3C)
    
    i2c_rtc = I2C(1, sda=Pin(2), scl=Pin(3), freq=400000)
    rtc = DS3231(i2c_rtc)
    
    return oled, rtc

# 显示函数：文字在顶部，时间在中间
def display_all(oled, rtc, scroll_offset):
    # 获取时间
    _, _, _, hour, minute, second, _ = rtc.get_time()
    
    # 清屏
    oled.fill(0)
    
    # 1. 顶部滚动文字
    x = scroll_offset
    y = 0
    for idx in range(14):
        if x + char_size > 0 and x < 128:
            draw_16x16_char(oled, idx, x, y)
        x += char_size + char_spacing
    
    # 2. 中间时间显示
    time_x = 19
    time_y = 20
    time_x = draw_time_char(oled, str(hour//10), time_x, time_y)
    time_x = draw_time_char(oled, str(hour%10), time_x, time_y)
    
    if second % 2 == 0:
        time_x = draw_time_char(oled, ":", time_x, time_y)
    else:
        time_x += 18
    
    time_x = draw_time_char(oled, str(minute//10), time_x, time_y)
    time_x = draw_time_char(oled, str(minute%10), time_x, time_y)
    
    oled.show()
    
    # 更新滚动偏移量
    scroll_offset -= scroll_speed
    total_width = 14 * (char_size + char_spacing) - char_spacing
    if scroll_offset < -total_width:
        scroll_offset = 128
    return scroll_offset

def main():
    try:
        oled, rtc = init_hardware()
        scroll_offset = 128
        
        # 首次使用设置时间
        # rtc.set_time(2025, 7, 27, 14, 0, 0, 1)
        
        # 初始LED状态设置
        control_leds()
        
        while True:
            # 刷新显示（文字和时间）
            scroll_offset = display_all(oled, rtc, scroll_offset)
            
            # 检查LED控制状态（不影响显示刷新频率）
            control_leds()
            
            time.sleep(0.05)
            
    except Exception as e:
        if 'oled' in locals():
            oled.fill(0)
            oled.text("Err", 55, 30)
            oled.show()
        print("错误:", e)
        time.sleep(5)

if __name__ == "__main__":
    main()
    