# 配置参数
char_spacing = 1  # 字间距
char_size = 16    # 16x16汉字
scroll_speed = 1  # 滚动速度（像素/帧）
purple = (128, 0, 128)  # 紫色RGB值

class OLED:
  def __init__(self):
    try:
      oled = self.init_hardware()
      scroll_offset = 128
        
      # 初始LED状态设置
      self.control_leds()
        
      while True:
        # 刷新显示（文字和时间）
        scroll_offset = self.display_all(oled, scroll_offset)
            
        # 检查LED控制状态（不影响显示刷新频率）
        self.control_leds()
            
        time.sleep(0.05)
            
    except Exception as e:
      if 'oled' in locals():
        oled.fill(0)
        oled.text("Err", 55, 30)
        oled.show()
      print("错误:", e)
      time.sleep(5)

  # 初始化硬件
  def init_hardware(self):
    i2c_oled = I2C(0, sda=Pin(0), scl=Pin(1), freq=400000)
    oled = SSD1306_I2C(128, 64, i2c_oled, addr=0x3C)
    
    return oled

  # LED控制函数
  def control_leds(slef):
    # 读取控制引脚状态
    if control_pin.value() == 1:  # 高电平
      # 所有灯珠设为紫色
      for i in range(LED_COUNT):
        np[i] = purple
      np.write()  # 刷新显示
    else:  # 低电平（默认）
      # 所有灯珠熄灭
      for i in range(LED_COUNT):
        np[i] = (0, 0, 0)
      np.write()  # 刷新显示

  # 显示函数：文字在顶部，时间在中间
  def display_all(self, oled, scroll_offset):
    # 清屏
    oled.fill(0)
    # 1. 顶部滚动文字
    x = scroll_offset
    y = 0
    for idx in range(14):
      if x + char_size > 0 and x < 128:
        self.draw_16x16_char(oled, idx, x, y)
      x += char_size + char_spacing
    
    oled.show()
    
    # 更新滚动偏移量
    scroll_offset -= scroll_speed
    total_width = 14 * (char_size + char_spacing) - char_spacing
    if scroll_offset < -total_width:
      scroll_offset = 128
    return scroll_offset

  # 绘制16x16汉字（阳码取模适配）
  def draw_16x16_char(self, oled, char_index, x, y):
    if 0 <= char_index < len(zh16x16):
      char_data = zh16x16[char_index]
      for col in range(16):
        byte1 = char_data[col * 2]
        byte2 = char_data[col * 2 + 1]
            
        for row in range(8):
          if byte1 & (1 << row):
            oled.pixel(x + col, y + row, 1)
        for row in range(8):
          if byte2 & (1 << row):
            oled.pixel(x + col, y + 8 + row, 1)


